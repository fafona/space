insert into storage.buckets (id, name, public)
values ('page-assets', 'page-assets', true)
on conflict (id) do update set public = excluded.public;

do $$
begin
  if not exists (
    select 1
    from pg_policies
    where schemaname = 'storage'
      and tablename = 'objects'
      and policyname = 'page_assets_public_read'
  ) then
    create policy page_assets_public_read
      on storage.objects
      for select
      to anon, authenticated
      using (bucket_id = 'page-assets');
  end if;

  if not exists (
    select 1
    from pg_policies
    where schemaname = 'storage'
      and tablename = 'objects'
      and policyname = 'page_assets_auth_upload'
  ) then
    create policy page_assets_auth_upload
      on storage.objects
      for insert
      to authenticated
      with check (bucket_id = 'page-assets');
  end if;
end
$$;

