-- merchant-space baseline schema for self-hosted Supabase / new Supabase project
-- Run in Supabase SQL Editor as project owner.

begin;

create extension if not exists pgcrypto;

create or replace function public.set_current_timestamp_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create table if not exists public.merchants (
  id text primary key,
  name text,
  email text,
  user_id uuid,
  auth_user_id uuid,
  owner_user_id uuid,
  owner_id uuid,
  auth_id uuid,
  created_by uuid,
  created_by_user_id uuid,
  owner_email text,
  contact_email text,
  user_email text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

drop trigger if exists set_merchants_updated_at on public.merchants;
create trigger set_merchants_updated_at
before update on public.merchants
for each row
execute function public.set_current_timestamp_updated_at();

create table if not exists public.pages (
  id uuid primary key default gen_random_uuid(),
  merchant_id text null,
  slug text not null default 'home',
  blocks jsonb not null default '[]'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

drop trigger if exists set_pages_updated_at on public.pages;
create trigger set_pages_updated_at
before update on public.pages
for each row
execute function public.set_current_timestamp_updated_at();

create table if not exists public.page_events (
  id bigint generated by default as identity primary key,
  event_type text not null,
  channel text null,
  page_path text null,
  success boolean null,
  payload_bytes integer null,
  changed_blocks integer null,
  reason text null,
  created_at timestamptz not null default now()
);

create index if not exists merchants_user_id_idx on public.merchants(user_id);
create index if not exists merchants_auth_user_id_idx on public.merchants(auth_user_id);
create index if not exists merchants_owner_user_id_idx on public.merchants(owner_user_id);
create index if not exists merchants_owner_id_idx on public.merchants(owner_id);
create index if not exists merchants_auth_id_idx on public.merchants(auth_id);
create index if not exists merchants_created_by_idx on public.merchants(created_by);
create index if not exists merchants_created_by_user_id_idx on public.merchants(created_by_user_id);
create index if not exists merchants_email_idx on public.merchants(lower(email));
create index if not exists merchants_owner_email_idx on public.merchants(lower(owner_email));
create index if not exists merchants_contact_email_idx on public.merchants(lower(contact_email));
create index if not exists merchants_user_email_idx on public.merchants(lower(user_email));

create index if not exists pages_slug_idx on public.pages(slug);
create index if not exists pages_merchant_id_idx on public.pages(merchant_id);
create index if not exists pages_updated_at_idx on public.pages(updated_at desc);
create unique index if not exists pages_public_home_unique_idx on public.pages(slug) where merchant_id is null and slug = 'home';
create unique index if not exists pages_merchant_slug_unique_idx on public.pages(merchant_id, slug) where merchant_id is not null;

create index if not exists page_events_created_at_idx on public.page_events(created_at desc);
create index if not exists page_events_event_type_idx on public.page_events(event_type);
create index if not exists page_events_channel_idx on public.page_events(channel);

grant usage on schema public to anon, authenticated;
grant usage, select on all sequences in schema public to anon, authenticated;
grant select on public.pages to anon;
grant select, insert, update, delete on public.pages to authenticated;
grant select, insert, update on public.merchants to authenticated;
grant select on public.page_events to authenticated;
grant insert on public.page_events to anon, authenticated;

alter table public.merchants enable row level security;
alter table public.pages enable row level security;
alter table public.page_events enable row level security;

drop policy if exists merchants_select_own on public.merchants;
create policy merchants_select_own on public.merchants
for select
to authenticated
using (
  (
    auth.uid() is not null
    and (
      id = auth.uid()::text
      or user_id = auth.uid()
      or auth_user_id = auth.uid()
      or owner_user_id = auth.uid()
      or owner_id = auth.uid()
      or auth_id = auth.uid()
      or created_by = auth.uid()
      or created_by_user_id = auth.uid()
    )
  )
  or (
    nullif(lower(coalesce(auth.jwt()->>'email', '')), '') is not null
    and (
      lower(coalesce(email, '')) = lower(coalesce(auth.jwt()->>'email', ''))
      or lower(coalesce(owner_email, '')) = lower(coalesce(auth.jwt()->>'email', ''))
      or lower(coalesce(contact_email, '')) = lower(coalesce(auth.jwt()->>'email', ''))
      or lower(coalesce(user_email, '')) = lower(coalesce(auth.jwt()->>'email', ''))
    )
  )
);

