import fs from "node:fs/promises";
import path from "node:path";

const PROVINCE_URL = "https://unpkg.com/province-city-china@8.5.8/dist/province.json";
const CITY_URL = "https://unpkg.com/province-city-china@8.5.8/dist/city.json";
const OUTPUT_PATH = path.join(process.cwd(), "src/lib/chinaLocationOptions.generated.ts");
const DIRECT_MUNICIPALITIES = new Set(["11", "12", "31", "50"]);
const SPECIAL_REGIONS = new Set(["71", "81", "82"]);

function toAscii(value) {
  return value.replace(/[^\x00-\x7F]/g, (char) => `\\u${char.charCodeAt(0).toString(16).padStart(4, "0")}`);
}

function formatAsTs(value) {
  const json = JSON.stringify(value, null, 2);
  return toAscii(json);
}

async function fetchJson(url) {
  const response = await fetch(url, { cache: "no-store" });
  if (!response.ok) {
    throw new Error(`failed to fetch ${url}: ${response.status} ${response.statusText}`);
  }
  return response.json();
}

async function main() {
  const [provinces, cities] = await Promise.all([fetchJson(PROVINCE_URL), fetchJson(CITY_URL)]);

  const cityMap = new Map();
  for (const city of cities) {
    const cityName = String(city.name ?? "").trim();
    const provinceCode = String(city.province ?? "").trim();
    const cityCode = String(city.city ?? "").trim();
    if (!cityName || !provinceCode || cityCode === "90") continue;
    if (cityName.includes("直辖县级行政区划")) continue;
    if (!cityMap.has(provinceCode)) cityMap.set(provinceCode, []);
    cityMap.get(provinceCode).push(cityName);
  }

  const country = {
    code: "CN",
    name: "China",
    provinces: provinces.map((province) => {
      const provinceCode = String(province.province ?? "").trim();
      const provinceName = String(province.name ?? "").trim();
      let provinceCities = cityMap.get(provinceCode) ?? [];
      if (DIRECT_MUNICIPALITIES.has(provinceCode) || SPECIAL_REGIONS.has(provinceCode)) {
        provinceCities = provinceName ? [provinceName] : [];
      }
      return {
        code: provinceCode,
        name: provinceName,
        cities: provinceCities,
      };
    }),
  };

  const fileContent =
    `// Generated by scripts/generate-china-location-options.mjs\n` +
    `type GeneratedCountryOption = { code: string; name: string; provinces: Array<{ code: string; name: string; cities: string[] }> };\n` +
    `export const CHINA_COUNTRY_OPTION: GeneratedCountryOption = ${formatAsTs(country)};\n`;
  await fs.writeFile(OUTPUT_PATH, fileContent, "utf8");
  console.log(`[generate-china-location-options] wrote ${OUTPUT_PATH}`);
}

main().catch((error) => {
  console.error(`[generate-china-location-options] ${error instanceof Error ? error.message : String(error)}`);
  process.exitCode = 1;
});
